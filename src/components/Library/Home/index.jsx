function stripLocale(path) {
  for (const locale of routing.locales) {
    if (locale === routing.defaultLocale) continue;
    if (path.startsWith(`/${locale}/`)) return path.slice(locale.length + 1);
    if (path === `/${locale}`) return "/";
  }
  return path;
}

